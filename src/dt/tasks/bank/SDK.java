package dt.tasks.bank;

// Интерфейс SDK для взаимодействия с аппаратурой банкомата
public interface SDK {

    /**
     * Подсчитать количество банкнот заданного номинала, оставшихся в кассетах.
     * Операция дорогая по времени, вызывать по возможности редко.
     *
     * @param banknote номинал банкнот (например, 50, 100 и т.д.)
     * @return количество оставшихся банкнот данного номинала
     */
    int countBanknotes(int banknote);

    /**
     * Переместить указанное количество банкнот заданного номинала в диспенсер для выдачи клиенту.
     *
     * @param banknote номинал банкнот
     * @param count    количество банкнот для перемещения
     */
    void moveBanknoteToDispenser(int banknote, int count);

    /**
     * Открыть диспенсер для выдачи купюр клиенту.
     */
    void openDispenser();

}
